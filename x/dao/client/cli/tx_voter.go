package cli

import (
	"strconv"

	"lordverse/x/dao/types"

	"github.com/cosmos/cosmos-sdk/client"
	"github.com/cosmos/cosmos-sdk/client/flags"
	"github.com/cosmos/cosmos-sdk/client/tx"
	"github.com/spf13/cobra"
)

func CmdCreateVoter() *cobra.Command {
	cmd := &cobra.Command{
		Use:   "create-voter [address] [weight]",
		Short: "Create a new voter",
		Args:  cobra.ExactArgs(2),
		RunE: func(cmd *cobra.Command, args []string) (err error) {
			argAddress := args[0]

			// convert string to uint32
			argWeight := args[1]
			weight, err := strconv.ParseUint(argWeight, 10, 32)
			if err != nil {
				return err
			}

			clientCtx, err := client.GetClientTxContext(cmd)
			if err != nil {
				return err
			}

			msg := types.NewMsgCreateVoter(clientCtx.GetFromAddress().String(), argAddress, uint32(weight))
			if err := msg.ValidateBasic(); err != nil {
				return err
			}
			return tx.GenerateOrBroadcastTxCLI(clientCtx, cmd.Flags(), msg)
		},
	}

	flags.AddTxFlagsToCmd(cmd)

	return cmd
}

